class loopprac
{
	public static void main (String args[])
 {  
    int j = 2;
 
	for(int a = 1; a<=10 ; a++)
	{
		System.out.println(j+"*"+a+"="+j*a);
	}
	System.out.println();
	
	int v = 3;
	
	for(int i = 1; i<=10; i++)
	{
		System.out.println(v+"*"+i+"="+v*i);
	}

	
 }	
 
}